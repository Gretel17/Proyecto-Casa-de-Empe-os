    private void tableProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseClicked
        int seleccion = this.tableProductos.rowAtPoint(evt.getPoint()); 
        CompartirDatosProducto comunicacion = new CompartirDatosProducto();
        CompartirDatosProducto.id_producto = Integer.valueOf(String.valueOf(tableProductos.getModel().getValueAt(seleccion, 0)));
        CompartirDatosProducto.nombre_producto = (String.valueOf(tableProductos.getModel().getValueAt(seleccion, 1)));
        CompartirDatosProducto.precio_producto = (String.valueOf(tableProductos.getModel().getValueAt(seleccion, 2)));
        CompartirDatosProducto.cantidad_disponible = Integer.valueOf(String.valueOf(tableProductos.getModel().getValueAt(seleccion, 3)));
        CompartirDatosProducto.tabla_productos_disponibles = this.tableProductos;
        dispose();
    }//GEN-LAST:event_tableProductosMouseClicked

    private void OcultarColumnas()
    {
        TableColumnModel tcm = this.tableProductos.getColumnModel();
        tcm.removeColumn(tcm.getColumn(0));
    }
